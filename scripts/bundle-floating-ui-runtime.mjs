import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { build } from "esbuild";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageDir = path.resolve(scriptDir, "..");
const outputPath = path.join(packageDir, "class-generation", "floating-ui.ts");

const entrySource = [
  "export {",
  "  arrow,",
  "  autoPlacement,",
  "  computePosition,",
  "  detectOverflow,",
  "  flip,",
  "  limitShift,",
  "  offset,",
  "  shift,",
  "} from \"@floating-ui/core\";",
  "",
].join("\n");

const result = await build({
  stdin: {
    contents: entrySource,
    loader: "ts",
    resolveDir: packageDir,
    sourcefile: "floating-ui-runtime-entry.ts",
  },
  bundle: true,
  charset: "utf8",
  format: "esm",
  legalComments: "none",
  minify: false,
  platform: "neutral",
  sourcemap: false,
  target: ["es2022"],
  treeShaking: true,
  write: false,
});

const bundled = result.outputFiles[0];
if (!bundled) {
  throw new Error("bundle-floating-ui-runtime: esbuild did not produce an output file");
}

const output = [
  "/* eslint-disable */",
  "// @ts-nocheck",
  "// This file is auto-generated by scripts/bundle-floating-ui-runtime.mjs.",
  "// It vendors the Floating UI core runtime into class-generation/ so generated",
  "// Playwright POM runtimes do not require consumers to install @floating-ui/*.",
  "",
  bundled.text,
].join("\n");

fs.writeFileSync(outputPath, output, "utf8");
console.log(`[bundle-floating-ui] wrote ${path.relative(packageDir, outputPath)}`);
